#include "libft.h"

int	ft_countn(const char *s, char c)
{
	int wn;
	int i;

	i = 0;
	wn = 0;
	while (*s)
	{
		while (s[i] == c)
			i++;
		if (s[i] != c)
		{
			while (s[i] != c)
				i++;
			wn++;
		}
	}
	return (wn);
}

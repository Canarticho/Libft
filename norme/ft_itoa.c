#include "libft.h"

char	*ft_itoa(int n)
{
	char	*str;
	int		i;
	int		neg;

	i = 0;
	str = ft_strnew(11);
	if (n < 0)
		 neg = 1;
	while (n)
	{
		str[9 - i++] = n % 10 + 'O';
		n = n / 10;
	}
	if (neg)
		str[9 - i++] = '-';
	str = str + (9 - i);
	return (str);
}
